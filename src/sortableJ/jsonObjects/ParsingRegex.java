package sortableJ.jsonObjects;

/**
 * 
 * @author Joshua Bowles
 *
 */
public class ParsingRegex {

	private String strongRegex;
	private String weakRegex;

	ParsingRegex() {

	}

	public String getStrongRegex() {
		return strongRegex;
	}

	public void setStrongRegex(String strongRegex) {
		this.strongRegex = strongRegex;
	}

	public String getWeakRegex() {
		return weakRegex;
	}

	public void setWeakRegex(String weakRegex) {
		this.weakRegex = weakRegex;
	}

}
